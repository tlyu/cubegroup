pub trait EdgesTrait<T: EdgeCyclesTrait>
    where Self: Clone + Copy + Debug + Default + Display
        + Eq + Hash + Mul + Mul<Turn> + Not + PartialEq + Sized,
        for<'a> &'a Self: Mul<&'a Self>,
        for<'a> &'a Self: Mul<Turn>,
        for<'a> &'a Self: Mul<&'a Turns>
{
    fn parity(&self) -> bool;
    fn cycles(&self) -> T;
    fn pack(&self) -> u64;
}
